## Handle `init` methods for records.
##
## Chapel semantics:
##   - A compiler-generated default initializer sets fields to their
##     default values.
##   - Users may provide their own `init()`.
##   - `new RecordName(args)` creates a record value.
##
## We generate:
##   1. A default `init()` if no user-defined one exists.
##   2. Field-default-value injection at the top of every `init`.
##   3. Convenience constructor procs: `RecordName.init(...)`,
##      `newRecordName(...)`, `RecordName.new(...)`.
