import std/[macros]
import std/[tables]

import recordinternal
import recordmethods
import recordvar
import recordutils

#[ Insert auto-generated init ]#

proc insertAutogeneratedInits(recDef: RecordDescription) =
  var hasInit = false
  for m in recDef.methods.definitions:
    if $m.definition.name == "init":
      hasInit = true
      break

  if not hasInit:
    var m = RecordMethod()
    m.definition = quote do:
      method init()
    m.body = newStmtList()
    recDef.methods.definitions.add(m)

#[ Inject field defaults into init bodies ]#

proc setupVarsInInit(recDef: RecordDescription) =
  # Build code that sets every field with a default value
  var initCode = newStmtList()
  for varDef in recDef.vars.definitions:
    let valueNode = varDef.definition[2]
    if valueNode.kind == nnkEmpty:
      continue
    let nameDef = varDef.definition.variableName
    initCode.add(quote do:
      this.`nameDef` = `valueNode`
    )

  for methodDef in recDef.methods.definitions:
    if $methodDef.definition.name != "init":
      continue
    if methodDef.isStatic or methodDef.insertUnmodified:
      continue

    # Set return type to the full record type (Name or Name[T,...])
    methodDef.definition.params[0] = recDef.fullType

    # Return `this` at the end
    methodDef.body.add(quote do:
      return this
    )

    # Inject field defaults at the top of the body
    if initCode.len > 0:
      methodDef.body.insert(0, initCode)

#[ Generate convenience constructors ]#

proc generateExtraInits(recDef: RecordDescription) =
  var newItems: seq[RecordMethod] = @[]
  for methodDef in recDef.methods.definitions:
    if $methodDef.definition.name != "init":
      continue

    let classType = recDef.fullType
    let underscore = ident"_"

    # 1. RecordName.init(...)  â†’  static proc
    block:
      let initName = ident"init"
      let m = RecordMethod()
      m.insertUnmodified = true
      m.definition = quote do:
        proc `initName`*(`underscore`: typedesc[`classType`]): `classType`
      m.body = quote do:
        var o: `classType`
        discard o.init()
        return o
      m.definition.addGenericParams(recDef)

      # Forward user-defined init params
      traverseParams(methodDef.definition, proc(idx: int, nameNode: NimNode,
          typeNode: NimNode, identDef: NimNode, identDefIdx: int) =
        m.definition.params.add(newIdentDefs(nameNode, typeNode, identDef[^1]))
        m.body[1][0].add(nameNode)  # DiscardStmt[0] = the o.init() call
      )
      newItems.add(m)

    # 2. newRecordName(...)
    block:
      let newName = ident("new" & $recDef.name)
      let m = RecordMethod()
      m.insertUnmodified = true
      m.definition = quote do:
        proc `newName`*(): `classType`
      m.body = quote do:
        var o: `classType`
        discard o.init()
        return o
      m.definition.addGenericParams(recDef)

      traverseParams(methodDef.definition, proc(idx: int, nameNode: NimNode,
          typeNode: NimNode, identDef: NimNode, identDefIdx: int) =
        m.definition.params.add(newIdentDefs(nameNode, typeNode, identDef[^1]))
        m.body[1][0].add(nameNode)  # DiscardStmt[0] = the o.init() call
      )
      newItems.add(m)

    # 3. RecordName.new(...)
    block:
      let newIdent = ident"new"
      let m = RecordMethod()
      m.insertUnmodified = true
      m.definition = quote do:
        proc `newIdent`*(`underscore`: typedesc[`classType`]): `classType`
      m.body = quote do:
        var o: `classType`
        discard o.init()
        return o
      m.definition.addGenericParams(recDef)

      traverseParams(methodDef.definition, proc(idx: int, nameNode: NimNode,
          typeNode: NimNode, identDef: NimNode, identDefIdx: int) =
        m.definition.params.add(newIdentDefs(nameNode, typeNode, identDef[^1]))
        m.body[1][0].add(nameNode)  # DiscardStmt[0] = the o.init() call
      )
      newItems.add(m)

  for m in newItems:
    recDef.methods.definitions.add(m)

#[ Register ]#

static:
  recordCompilerHooks.add(proc(stage: RecordCompilerStage, recDef: RecordDescription) =
    if stage == RecordAddExtraDefinitions: insertAutogeneratedInits(recDef)
    if stage == RecordAddExtraDefinitions: generateExtraInits(recDef)
    if stage == RecordModifyDefinitions: setupVarsInInit(recDef)
  )
