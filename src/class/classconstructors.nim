## Handle ``init`` methods for classes.
##
## Chapel semantics:
##   - A compiler-generated default initializer sets fields to their default
##     values and allocates the ``ref object``.
##   - Users may provide their own ``init()``.
##   - ``new ClassName(args)`` creates a class instance.
##   - For inherited classes, parent fields are initialized first (via
##     ``super.init()``); if the user doesn't call it, the compiler inserts
##     a zero-argument ``super.init()`` call at the start.
##   - A ``postinit()`` method, if defined, is called after ``init()``.
##
## We generate:
##   1. A default ``init()`` if no user-defined one exists.
##   2. Field-default-value injection at the top of every ``init``.
##   3. Convenience constructor procs: ``ClassName.init(...)``,
##      ``newClassName(...)``, ``ClassName.new(...)``.
